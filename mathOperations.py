# basic math operation
num1 = 5
num2 = 2

# addition
print(num1 + num2)  # 5+2=7

# subtraction
print(num1 - num2)  # 5-2=3

# multiplication
print(num1 * num2)  # 5*2=10

# division
print(num1 / num2)  # 10/2=5.0

# absolute division
print(num1 // num2)  # 10//2=5

# power
print(num1 ** num2)  # 5**2=25

# modulus (gives remainder)
print(num1 % num2)  # 5%2=1
