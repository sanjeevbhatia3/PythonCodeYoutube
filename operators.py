# comparison operators
num1 = 5
num2 = 8
print(num1 == num2)
print(num1 != num2)
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)

print('hi' == 'hi')
print('hi' == 'Hi')

# logical operators
print(10 < 15 and 18 > 16)  # True and True -> True
print(10 < 15 and 18 < 16)  # True and False -> False
print(10 < 15 or 18 < 16)  # True or False -> True
print(not 10 < 15)
